    void func(int a)
    {
        switch(opt)
        {
            case 1:N+=N1;N1=0;opt=a;break;
            case 2:N-=N1;N1=0;opt=a;break;
            case 3:N*=N1;N1=0;opt=a;break;
            case 4:N/=N1;N1=0;opt=a;break;
        }
    }
void minus()
{
    if(txt.getText().isEmpty())
    {  
        opt=2;
        String a=lbl.getText();
        int a1=a.length();
        a=a.substring(0,a1-1)+"-";
        lbl.setText(a);
    }
    if(N!=0)
    {
        N1=Float.parseFloat(txt.getText());
        func(2);
        lbl.setText(lbl.getText()+txt.getText()+"-");
        txt.setText("");
    }
    else
    {
        N=Float.parseFloat(txt.getText());
        lbl.setText(txt.getText()+"-");
        txt.setText("");
        opt=2;
    }
    txt.grabFocus();
}
